public class DataType {
    public static void main(String[] args){
        int a = 41;
        int b = 4;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        a++;
        b--;
        System.out.println(a);
        System.out.println(b);


        int g = 5;
        int h = 2;
        System.out.println(g/h);
        System.out.println(g%h);
    }
}
